// here we will use the bracket notation to access the position of an element in an array

const fruits = ['banana', 'lemon', 'kiwi', 'mango', 'apple'];

// getting the length/ number of items in the array

let arrLength = fruits.length;
console.log(arrLength);

// accessing a specific item in an array using the bracket notation
let favFruit = fruits[0];
console.log(favFruit);